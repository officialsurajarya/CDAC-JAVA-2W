public class assignOper {
    public static void main(String[] args) {
        
        int x = 10;
        x+=5; //15
        System.out.println(x);

        int y=10;
        y-=3; //7
        System.out.println(y);

        int i=10;
        i*=2; //20
        System.out.println(i);

        int j = 10;
        j/=4; //8
        System.out.println(j);
    }
}
